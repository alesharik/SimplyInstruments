package SimplyTools.API;

public class PatternColorMap {

	public ARGB defColor;
	public ARGB blackColor;
	public ARGB cornerColor;

	public PatternColorMap(ARGB defColor, ARGB blackColor, ARGB cornerColor) {
		this.defColor = defColor;
		this.blackColor = blackColor;
		this.cornerColor = cornerColor;
	}

}
